-- Multi-trophy mount master
CREATE TABLE IF NOT EXISTS multi_trophies (
  id    UUID    PRIMARY KEY DEFAULT uuid_generate_v4(),
  name  TEXT    NOT NULL UNIQUE,
  size  TEXT    NOT NULL CHECK (size IN ('極小', '小', '中', '大', '特大', '極大')),
  cost  INTEGER NOT NULL CHECK (cost > 0)
);

-- Required animal trophies per multi-trophy (1 row per animal slot)
CREATE TABLE IF NOT EXISTS multi_trophy_requirements (
  id              UUID    PRIMARY KEY DEFAULT uuid_generate_v4(),
  multi_trophy_id UUID    NOT NULL REFERENCES multi_trophies(id) ON DELETE CASCADE,
  animal_id       UUID    NOT NULL REFERENCES animals(id)        ON DELETE RESTRICT,
  gender          TEXT    CHECK (gender IN ('male', 'female')),
  sort_order      INTEGER NOT NULL DEFAULT 0,
  UNIQUE NULLS NOT DISTINCT (multi_trophy_id, animal_id, gender, sort_order)
);

-- RLS
ALTER TABLE multi_trophies            ENABLE ROW LEVEL SECURITY;
ALTER TABLE multi_trophy_requirements ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "public read multi_trophies"            ON multi_trophies;
DROP POLICY IF EXISTS "public read multi_trophy_requirements" ON multi_trophy_requirements;

CREATE POLICY "public read multi_trophies"            ON multi_trophies            FOR SELECT USING (true);
CREATE POLICY "public read multi_trophy_requirements" ON multi_trophy_requirements FOR SELECT USING (true);

