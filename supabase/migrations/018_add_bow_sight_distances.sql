-- Bow sight distance table
-- grain: arrow weight in grains (300/420/600)
-- pin: sight pin number (1=top ... 5=bottom), pin3 hits zero_distance when zeroed
-- distance: arrow reach in meters for that pin/grain/zero combination

CREATE TABLE bow_sight_distances (
  id            UUID    PRIMARY KEY DEFAULT uuid_generate_v4(),
  firearm_id    UUID    NOT NULL REFERENCES firearms(id) ON DELETE CASCADE,
  zero_distance INTEGER NOT NULL,
  grain         INTEGER NOT NULL CHECK (grain IN (300, 420, 600)),
  pin           INTEGER NOT NULL CHECK (pin BETWEEN 1 AND 5),
  distance      INTEGER NOT NULL
);

ALTER TABLE bow_sight_distances ENABLE ROW LEVEL SECURITY;
CREATE POLICY "public_read_bow_sight_distances" ON bow_sight_distances FOR SELECT USING (true);

