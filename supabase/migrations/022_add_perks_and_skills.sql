-- =====================================================
-- PERKS (パーク)
-- =====================================================

CREATE TABLE IF NOT EXISTS perks (
  id              UUID    PRIMARY KEY DEFAULT uuid_generate_v4(),
  name            TEXT    NOT NULL,
  category        TEXT    NOT NULL CHECK (category IN ('ライフル', 'ハンドガン', 'ショットガン', 'アーチェリー')),
  activation_type TEXT    NOT NULL CHECK (activation_type IN ('passive', 'active')),
  max_level       INTEGER NOT NULL DEFAULT 1,
  cooldown        INTEGER,        -- seconds; active perks only
  comment         TEXT,
  sort_order      INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS perk_levels (
  id          UUID    PRIMARY KEY DEFAULT uuid_generate_v4(),
  perk_id     UUID    NOT NULL REFERENCES perks(id) ON DELETE CASCADE,
  level       INTEGER NOT NULL CHECK (level >= 1),
  description TEXT    NOT NULL,
  UNIQUE (perk_id, level)
);

-- =====================================================
-- SKILLS (スキル)
-- =====================================================

CREATE TABLE IF NOT EXISTS skills (
  id              UUID    PRIMARY KEY DEFAULT uuid_generate_v4(),
  name            TEXT    NOT NULL,
  category        TEXT    NOT NULL CHECK (category IN ('ストーカー', 'アンブッシャー')),
  tier            INTEGER NOT NULL DEFAULT 0 CHECK (tier BETWEEN 0 AND 4),
  activation_type TEXT    NOT NULL CHECK (activation_type IN ('passive', 'active')),
  max_level       INTEGER NOT NULL DEFAULT 1,
  cooldown        INTEGER,        -- seconds; active skills only
  comment         TEXT,
  sort_order      INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS skill_levels (
  id          UUID    PRIMARY KEY DEFAULT uuid_generate_v4(),
  skill_id    UUID    NOT NULL REFERENCES skills(id) ON DELETE CASCADE,
  level       INTEGER NOT NULL CHECK (level >= 1),
  description TEXT    NOT NULL,
  UNIQUE (skill_id, level)
);

-- =====================================================
-- RLS
-- =====================================================

ALTER TABLE perks        ENABLE ROW LEVEL SECURITY;
ALTER TABLE perk_levels  ENABLE ROW LEVEL SECURITY;
ALTER TABLE skills       ENABLE ROW LEVEL SECURITY;
ALTER TABLE skill_levels ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "public read perks"        ON perks;
DROP POLICY IF EXISTS "public read perk_levels"  ON perk_levels;
DROP POLICY IF EXISTS "public read skills"       ON skills;
DROP POLICY IF EXISTS "public read skill_levels" ON skill_levels;

CREATE POLICY "public read perks"        ON perks        FOR SELECT USING (true);
CREATE POLICY "public read perk_levels"  ON perk_levels  FOR SELECT USING (true);
CREATE POLICY "public read skills"       ON skills       FOR SELECT USING (true);
CREATE POLICY "public read skill_levels" ON skill_levels FOR SELECT USING (true);

