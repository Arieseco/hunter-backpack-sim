-- =====================================================
-- HUNTING DOGS (狩猟犬)
-- =====================================================

CREATE TABLE IF NOT EXISTS hunting_dogs (
  id          UUID    PRIMARY KEY DEFAULT uuid_generate_v4(),
  name        TEXT    NOT NULL,
  description TEXT,
  notes       TEXT,
  sort_order  INT     NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS dog_abilities (
  id          UUID    PRIMARY KEY DEFAULT uuid_generate_v4(),
  dog_id      UUID    NOT NULL REFERENCES hunting_dogs(id) ON DELETE CASCADE,
  name        TEXT    NOT NULL,
  description TEXT,
  sort_order  INT     NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS dog_traits (
  id          UUID    PRIMARY KEY DEFAULT uuid_generate_v4(),
  dog_id      UUID    NOT NULL REFERENCES hunting_dogs(id) ON DELETE CASCADE,
  level       INT     NOT NULL CHECK (level IN (5, 10, 15, 20, 25, 30)),
  slot        INT     NOT NULL CHECK (slot IN (1, 2)),
  name        TEXT    NOT NULL,
  description TEXT,
  UNIQUE (dog_id, level, slot)
);

ALTER TABLE hunting_dogs  ENABLE ROW LEVEL SECURITY;
ALTER TABLE dog_abilities ENABLE ROW LEVEL SECURITY;
ALTER TABLE dog_traits    ENABLE ROW LEVEL SECURITY;

CREATE POLICY "public read hunting_dogs"  ON hunting_dogs  FOR SELECT USING (true);
CREATE POLICY "public read dog_abilities" ON dog_abilities FOR SELECT USING (true);
CREATE POLICY "public read dog_traits"    ON dog_traits    FOR SELECT USING (true);

